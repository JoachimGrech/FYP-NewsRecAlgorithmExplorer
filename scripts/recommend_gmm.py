# Module Test Implementation
# ---------------------------------------------------------------------------
def main():
    all_articles = load_json(DATA_PATH)
    users        = load_json(USERS_PATH)

    if not all_articles or not users:
        print("Required data files not found. Run previous pipeline scripts first.")
        return

    # Count how many articles have GMM vectors
    gmm_count = sum(1 for a in all_articles if a.get('gmm_topic_vector'))
    if gmm_count == 0:
        print("No GMM topic vectors found in news_data.json.")
        print("Run:  python scripts/run_topic_modeling.py --method gmm")
        return
    print(f"GMM vectors available on {gmm_count} / {len(all_articles)} articles.\n")

    # Build a link → article index so the profile builder can fetch current GMM vectors
    art_index = build_article_index(all_articles)

    test_user = users[0]
    print(f"======== GMM Recommendation Engine — Test ========")
    print(f"User    : {test_user['name']}")
    print(f"Persona : {test_user['description']}")

    # TEST 1: Pure relevance (diversity = 0)
    print("\n--- TEST 1: PURE RELEVANCE (diversity = 0.0) ---")
    recs, user_vec = recommend_articles_gmm(
        test_user, all_articles, num_recommendations=5, diversity_score=0.0,
        article_index=art_index
    )
    top_cluster = int(np.argmax(user_vec))
    print(f"User's top GMM cluster: topic_{top_cluster}  "
          f"(weight={user_vec[top_cluster]:.3f})")
    for rank, (score, sim, article) in enumerate(recs, 1):
        dom = max(article['gmm_topic_vector'], key=article['gmm_topic_vector'].get)
        dom_label = dom.replace('topic_', 'Cluster ')
        print(f"[{rank}] (sim={sim:.3f}) {article['title'][:70]}... [{dom_label}]")

    # TEST 2: High diversity
    print("\n--- TEST 2: HIGH DIVERSITY (diversity = 0.8) ---")
    recs, _ = recommend_articles_gmm(
        test_user, all_articles, num_recommendations=5, diversity_score=0.8,
        article_index=art_index
    )
    for rank, (score, sim, article) in enumerate(recs, 1):
        dom = max(article['gmm_topic_vector'], key=article['gmm_topic_vector'].get)
        dom_label = dom.replace('topic_', 'Cluster ')
        print(f"[{rank}] (final={score:.3f} | sim={sim:.3f}) "
              f"{article['title'][:70]}... [{dom_label}]")

    print("\nGMM Recommendation Framework initialisation tests completed.")


if __name__ == "__main__":
    main()
